import { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import { useSetRecoilState } from "recoil";
import { authAtom } from "../recoil/authAtom";
import { FiUser, FiMail, FiPhone, FiLock, FiUserPlus } from "react-icons/fi";
import toast from "react-hot-toast";
import api from "../api/axios";
import InputField from "../components/ui/InputField";
import Button from "../components/ui/Button";

const delay = (ms) => new Promise((res) => setTimeout(res, ms));

export default function Register() {
  const [formData, setFormData] = useState({
    name: "",
    email: "",
    phone: "",
    password: "",
    confirmPassword: "",
  });
  const [errors, setErrors] = useState({});
  const [loading, setLoading] = useState(false);
  const setAuth = useSetRecoilState(authAtom);
  const navigate = useNavigate();

  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData((f) => ({ ...f, [name]: value }));
    if (errors[name]) setErrors((e) => ({ ...e, [name]: "" }));
  };

  const validate = () => {
    const errs = {};

    if (!formData.name.trim()) {
      errs.name = "–ò–º–µ—Ç–æ –µ –∑–∞–¥—ä–ª–∂–∏—Ç–µ–ª–Ω–æ";
    } else if (formData.name.trim().length < 2) {
      errs.name = "–ò–º–µ—Ç–æ —Ç—Ä—è–±–≤–∞ –¥–∞ –µ –ø–æ–Ω–µ 2 —Å–∏–º–≤–æ–ª–∞";
    }

    if (!formData.email.trim()) {
      errs.email = "–ò–º–µ–π–ª—ä—Ç –µ –∑–∞–¥—ä–ª–∂–∏—Ç–µ–ª–µ–Ω";
    } else if (!/\S+@\S+\.\S+/.test(formData.email)) {
      errs.email = "–ù–µ–≤–∞–ª–∏–¥–µ–Ω –∏–º–µ–π–ª –∞–¥—Ä–µ—Å";
    }

    if (!formData.phone.trim()) {
      errs.phone = "–¢–µ–ª–µ—Ñ–æ–Ω–Ω–∏—è—Ç –Ω–æ–º–µ—Ä –µ –∑–∞–¥—ä–ª–∂–∏—Ç–µ–ª–µ–Ω";
    } else {
      const phoneRegex = /^(\+359|0)[0-9]{8,9}$/;
      const cleanPhone = formData.phone.replace(/\s/g, "");
      if (!phoneRegex.test(cleanPhone)) {
        errs.phone = "–ù–µ–≤–∞–ª–∏–¥–µ–Ω —Ç–µ–ª–µ—Ñ–æ–Ω–µ–Ω –Ω–æ–º–µ—Ä (–Ω–∞–ø—Ä. 0888123456)";
      }
    }

    if (!formData.password) {
      errs.password = "–ü–∞—Ä–æ–ª–∞—Ç–∞ –µ –∑–∞–¥—ä–ª–∂–∏—Ç–µ–ª–Ω–∞";
    } else if (formData.password.length < 6) {
      errs.password = "–ü–∞—Ä–æ–ª–∞—Ç–∞ —Ç—Ä—è–±–≤–∞ –¥–∞ –±—ä–¥–µ –ø–æ–Ω–µ 6 —Å–∏–º–≤–æ–ª–∞";
    }

    if (formData.password !== formData.confirmPassword) {
      errs.confirmPassword = "–ü–∞—Ä–æ–ª–∏—Ç–µ –Ω–µ —Å—ä–≤–ø–∞–¥–∞—Ç";
    }

    setErrors(errs);
    return Object.keys(errs).length === 0;
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    if (!validate()) {
      toast.error("–ú–æ–ª—è, –ø–æ–ø—Ä–∞–≤–µ—Ç–µ –≥—Ä–µ—à–∫–∏—Ç–µ –≤—ä–≤ —Ñ–æ—Ä–º–∞—Ç–∞");
      return;
    }

    setLoading(true);
    const MIN_VISIBLE = 1500;
    const start = Date.now();
    const loadingToast = toast.loading("üîÑ –°—ä–∑–¥–∞–≤–∞–Ω–µ –Ω–∞ –ø—Ä–æ—Ñ–∏–ª‚Ä¶");

    try {
      const res = await api.post("/api/auth/register", {
        name: formData.name,
        email: formData.email,
        phone: formData.phone.replace(/\s/g, ""),
        password: formData.password,
      });

      const elapsed = Date.now() - start;
      if (elapsed < MIN_VISIBLE) {
        await delay(MIN_VISIBLE - elapsed);
      }

      toast.dismiss(loadingToast);

      if (res.data.success) {
        localStorage.setItem("token", res.data.token);
        localStorage.setItem("user", JSON.stringify(res.data.user));
        setAuth({ user: res.data.user, token: res.data.token });

        toast.success(`üéâ –î–æ–±—Ä–µ –¥–æ—à–ª–∏, ${res.data.user.name}!`, {
          duration: 6000,
          style: {
            background: "#065f46",
            color: "#d1fae5",
            border: "2px solid #10b981",
            padding: "16px",
            borderRadius: "12px",
            maxWidth: "400px",
          },
        });

        setTimeout(() => navigate("/"), 800);
      } else {
        toast.error(res.data.message || "‚ùå –ì—Ä–µ—à–∫–∞ –ø—Ä–∏ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è");
      }
    } catch (err) {
      const elapsed = Date.now() - start;
      if (elapsed < MIN_VISIBLE) {
        await delay(MIN_VISIBLE - elapsed);
      }
      toast.dismiss(loadingToast);

      if (err.response?.data?.message) {
        toast.error(err.response.data.message);
      } else if (err.code === "NETWORK_ERROR" || !err.response) {
        toast.error("–ü—Ä–æ–±–ª–µ–º —Å –º—Ä–µ–∂–æ–≤–∞—Ç–∞ –≤—Ä—ä–∑–∫–∞. –ü—Ä–æ–≤–µ—Ä–µ—Ç–µ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç.");
      } else {
        toast.error("–í—ä–∑–Ω–∏–∫–Ω–∞ –Ω–µ–æ—á–∞–∫–≤–∞–Ω–∞ –≥—Ä–µ—à–∫–∞.");
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-900 p-6 pt-20">
      <div className="w-full max-w-md bg-gray-800 p-8 rounded-xl shadow-lg space-y-6">
        <h2 className="text-3xl font-extrabold text-white text-center">
          –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è
        </h2>

        <form onSubmit={handleSubmit} className="space-y-4">
          <InputField
            id="name"
            name="name"
            type="text"
            autoComplete="name"
            icon={FiUser}
            placeholder="–ü—ä–ª–Ω–æ –∏–º–µ"
            value={formData.name}
            onChange={handleChange}
            error={errors.name}
            disabled={loading}
          />

          <InputField
            id="email"
            name="email"
            type="email"
            autoComplete="email"
            icon={FiMail}
            placeholder="–ò–º–µ–π–ª –∞–¥—Ä–µ—Å"
            value={formData.email}
            onChange={handleChange}
            error={errors.email}
            disabled={loading}
          />

          <InputField
            id="phone"
            name="phone"
            type="tel"
            autoComplete="tel"
            icon={FiPhone}
            placeholder="–¢–µ–ª–µ—Ñ–æ–Ω–µ–Ω –Ω–æ–º–µ—Ä"
            value={formData.phone}
            onChange={handleChange}
            error={errors.phone}
            disabled={loading}
          />

          <InputField
            id="password"
            name="password"
            type="password"
            autoComplete="new-password"
            icon={FiLock}
            placeholder="–ü–∞—Ä–æ–ª–∞ (–º–∏–Ω–∏–º—É–º 6 —Å–∏–º–≤–æ–ª–∞)"
            value={formData.password}
            onChange={handleChange}
            error={errors.password}
            disabled={loading}
          />

          <InputField
            id="confirmPassword"
            name="confirmPassword"
            type="password"
            autoComplete="new-password"
            icon={FiLock}
            placeholder="–ü–æ—Ç–≤—ä—Ä–¥–µ—Ç–µ –ø–∞—Ä–æ–ª–∞—Ç–∞"
            value={formData.confirmPassword}
            onChange={handleChange}
            error={errors.confirmPassword}
            disabled={loading}
          />

          <Button
            type="submit"
            variant="primary"
            size="large"
            loading={loading}
            icon={FiUserPlus}
            className="w-full"
          >
            {loading ? "–°—ä–∑–¥–∞–≤–∞–Ω–µ –Ω–∞ –ø—Ä–æ—Ñ–∏–ª..." : "–°—ä–∑–¥–∞–π –ø—Ä–æ—Ñ–∏–ª"}
          </Button>
        </form>

        <p className="text-center text-gray-400">
          –í–µ—á–µ –∏–º–∞—Ç–µ –ø—Ä–æ—Ñ–∏–ª?{" "}
          <Link
            to="/login"
            className="text-blue-400 hover:text-blue-300 font-medium"
          >
            –í–ª–µ–∑—Ç–µ —Ç—É–∫
          </Link>
        </p>

        <p className="text-center text-xs text-gray-500">
          –° —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è—Ç–∞ —Å–∏ –ø—Ä–∏–µ–º–∞—Ç–µ –Ω–∞—à–∏—Ç–µ{" "}
          <Link to="/terms" className="text-blue-400 hover:text-blue-300">
            –£—Å–ª–æ–≤–∏—è –∑–∞ –ø–æ–ª–∑–≤–∞–Ω–µ
          </Link>{" "}
          –∏{" "}
          <Link to="/privacy" className="text-blue-400 hover:text-blue-300">
            –ü–æ–ª–∏—Ç–∏–∫–∞ –∑–∞ –ø–æ–≤–µ—Ä–∏—Ç–µ–ª–Ω–æ—Å—Ç
          </Link>
          .
        </p>
      </div>
    </div>
  );
}
